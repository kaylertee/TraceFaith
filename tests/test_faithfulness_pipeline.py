class RecordingTraceGenerator:
    model_name = "recording-target"

    def __init__(self) -> None:
        self.calls = 0

    def generate_trace(self, example) -> StructuredTrace:
        self.calls += 1
        return StructuredTrace(
            visual_evidence=[{"modality": "image", "content": "The image contains the relevant visual evidence."}],
            textual_evidence=[{"modality": "text", "content": example.context or example.question}],
            binding_claim={
                "text": "The visual evidence is bound to the condition named in the question.",
                "bound_answer_option": example.correct_answer or "UNKNOWN",
            },
            scientific_principle={"text": "Use the supplied scientific principle."},
            inference={"text": "Apply the principle to the evidence."},
            conclusion=f"The reasoning supports answer {example.correct_answer or 'UNKNOWN'}.",
            final_answer=example.correct_answer or "UNKNOWN",
        )


class RecordingJudge:
    model_name = "recording-judge"

    def __init__(self) -> None:
        self.calls = 0

    def judge(self, intervention_id, trace, example) -> JudgeOutput:
        self.calls += 1
        return JudgeOutput(
            intervention_id=intervention_id,
            judge_model=self.model_name,
            faithfulness_score=5,
            is_intervened_or_flawed=False,
            flawed_component="none",
        )


class RecordingTargetEvaluator:
    model_name = "recording-target-rerun"

    def __init__(self) -> None:
        self.calls = 0

    def evaluate(self, intervention_id, trace, example, condition="original_trace", include_image=True) -> TargetOutput:
        self.calls += 1
        final_answer = trace.final_answer if trace is not None else example.correct_answer or "UNKNOWN"
        if trace is not None and condition == "retargeted_support":
            final_answer = trace.binding_claim.bound_answer_option or final_answer
        return TargetOutput(
            intervention_id=intervention_id,
            target_model=self.model_name,
            final_answer=final_answer,
            is_correct=final_answer == example.correct_answer if example.correct_answer else None,
            metadata={"condition": condition, "include_image": include_image},
        )


class RecordingInterventionModel:
    model_name = "recording-intervention"

    def __init__(self) -> None:
        self.calls = 0

    def generate_interventions(self, example, original_trace, template_records) -> InterventionModelOutput:
        self.calls += 1
        interventions = []
        for record in template_records:
            if record.intervention.intervention_type == "original_trace":
                continue
            interventions.append(
                {
                    "intervention_type": record.intervention.intervention_type,
                    "intervened_trace": record.intervened_trace.model_dump(mode="json"),
                    "notes": "generated by fake intervention model",
                }
            )
        return InterventionModelOutput(
            example_id=example.example_id,
            intervention_model=self.model_name,
            raw_output=json.dumps({"interventions": interventions}),
        )


class FailingSecondExampleInterventionModel(RecordingInterventionModel):
    def __init__(self, failing_example_id: str) -> None:
        super().__init__()
        self.failing_example_id = failing_example_id

    def generate_interventions(self, example, original_trace, template_records) -> InterventionModelOutput:
        if example.example_id == self.failing_example_id:
            raise RuntimeError("simulated intervention failure")
        return super().generate_interventions(example, original_trace, template_records)



class FailingSecondExampleTraceGenerator(RecordingTraceGenerator):
    def __init__(self, failing_example_id: str) -> None:
        super().__init__()
        self.failing_example_id = failing_example_id

    def generate_trace(self, example) -> StructuredTrace:
        if example.example_id == self.failing_example_id:
            raise RuntimeError("simulated trace failure")
        return super().generate_trace(example)


class FailingSecondExampleTargetEvaluator(RecordingTargetEvaluator):
    def __init__(self, failing_example_id: str) -> None:
        super().__init__()
        self.failing_example_id = failing_example_id

    def evaluate(self, intervention_id, trace, example, condition="original_trace", include_image=True) -> TargetOutput:
        if example.example_id == self.failing_example_id:
            raise RuntimeError("simulated target failure")
        return super().evaluate(intervention_id, trace, example, condition=condition, include_image=include_image)


def _count_by_example_id(intervention_ids) -> dict[str, int]:
    counts: dict[str, int] = {}
    for intervention_id in intervention_ids:
        example_id = intervention_id.split("::")[0]
        counts[example_id] = counts.get(example_id, 0) + 1
    return counts


